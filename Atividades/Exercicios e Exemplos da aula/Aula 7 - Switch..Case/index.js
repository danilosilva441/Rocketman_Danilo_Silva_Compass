let permissao = "Gerente"; //Comum, gerente, diretor

switch (permissao){
    case 'Comum':
        console.log('Usuario Comum');
        break;
    case 'Gerente':
        console.log('Usuario Gerente');
        break;
    case 'Diretor':
        console.log('Usuario Diretor');
        break;
    default:
        console.log('Usuário não reconhecido!')
}

